export const useAnimeData = () => {
  const [error, setError] = useState('');

  const getAnimeByGenre = (genre) => {
    setError('');

    const recommendations = animeDatabase.filter(anime =>
      anime.genres.includes(genre)
    );

    if (recommendations.length === 0) {
      setError(`На жаль, для жанру ${genre} ще немає рекомендацій`);
      return [];
    }

    return recommendations; 
  };

  return {
    animeList: animeDatabase,
    error,
    getAnimeByGenre
  };
};